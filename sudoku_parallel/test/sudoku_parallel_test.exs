defmodule SudokuParallelTest do
  use ExUnit.Case
  doctest SudokuParallel

  test "the truth" do
    test_string = "200370009009200007001004002050000800008000900006000040900100500800007600400089001"
    assert SudokuParallel.solve(test_string) == "284375169639218457571964382152496873348752916796831245967143528813527694425689731"
  end
end
